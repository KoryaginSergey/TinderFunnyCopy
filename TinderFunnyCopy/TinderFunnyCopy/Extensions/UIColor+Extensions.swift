import Foundation
import UIKit


extension UIColor {
  class var myBlueColor: UIColor {
      return UIColor(red: 50/255, green: 182/255, blue: 217/255, alpha: 1.0)
  }
  class var myRedColor: UIColor {
      return UIColor(red: 248/255, green: 52/255, blue: 56/255, alpha: 1.0)
  }
  class var myGreenColor: UIColor {
      return UIColor(red: 112/255, green: 220/255, blue: 1/255, alpha: 1.0)
  }
  class var myGreyColor: UIColor {
      return UIColor(red: 85/255, green: 85/255, blue: 85/255, alpha: 0.5)
  }
}
